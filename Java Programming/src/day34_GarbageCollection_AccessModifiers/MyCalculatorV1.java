package day34_GarbageCollection_AccessModifiers;

public class MyCalculatorV1 {

    public int add(int n1, int n2){
        int result=n1+n2;
        return result;
    }

    public int minus(int n1, int n2){
        int result=n1-n2;
        return result;
    }

    public int multiply(int n1, int n2){
        int result=n1*n2;
        return result;
    }

    public int divide(int n1, int n2){
        int result=n1/n2;
        return result;
    }
}
