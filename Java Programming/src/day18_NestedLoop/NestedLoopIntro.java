package day18_NestedLoop;

public class NestedLoopIntro {

    public static void main(String[] args) {

        for (int i=0;i<=5;i++){
            System.out.println("WoodenSpoon");
        }
        for (int i=0;i<=5;i++){
            System.out.println("WoodenSpoon");
        }
        for (int i=0;i<=5;i++){
            System.out.println("WoodenSpoon");
        }
        for (int i=0;i<=5;i++){
            System.out.println("WoodenSpoon");
        }
        System.out.println("-----------------------------------------");
        for (int j = 0; j <=20 ; j++) {
            for (int i = 0; i <= 5; i++) {
                System.out.println("WoodenSpoon");


            }
        }





    }
}
